#!/usr/bin/env bash
# ==============================================================================
# 1단계: 지정된 Git 저장소에서 소스 업데이트 (git pull)
# 2단계: 최신 설정 파일(configuration.conf)을 타겟 경로로 복사
# ==============================================================================

# ------------------------------------------------------------------------------
# 1. 환경 변수 설정 (사용자 환경에 맞게 실제 경로로 수정해 주세요)
# ------------------------------------------------------------------------------
GIT_DIR="/path/to/your/git_repository"
SRC_DIR="/path/to/your/source_dir"
TGT_DIR="/path/to/your/target_dir"

# ------------------------------------------------------------------------------
# 2. [1단계] Git 소스 업데이트
# ------------------------------------------------------------------------------
echo "============================================================"
echo "[1/2] Git 저장소 업데이트 진행..."
echo "============================================================"

# GIT_DIR 디렉토리가 존재하는지 확인
if [ ! -d "$GIT_DIR" ]; then
  echo "❌ 오류: GIT_DIR 경로($GIT_DIR)를 찾을 수 없습니다." >&2
  exit 1
fi

# 해당 디렉토리로 이동
cd "$GIT_DIR" || { 
  echo "❌ 오류: $GIT_DIR 경로로 이동할 수 없습니다." >&2
  exit 1 
}

# git pull 실행 및 오류 제어
# (git pull 자체의 상세 에러 메세지는 화면에 그대로 출력되며, 실패 시 아래 echo 문구 출력 후 스크립트 중단)
if ! git pull; then
  echo "" >&2
  echo "❌ [중단] 'git pull' 실행 중 오류가 발생했습니다. 이후 작업을 취소합니다." >&2
  exit 1
fi

echo "✅ Git 업데이트 완료."
echo ""


# ------------------------------------------------------------------------------
# 3. [2단계] 설정 파일(configuration.conf) 복사
# ------------------------------------------------------------------------------
echo "============================================================"
echo "[2/2] 설정 파일(configuration.conf) 복사 진행..."
echo "============================================================"

SRC_FILE="$SRC_DIR/configuration.conf"

# 원본 파일이 존재하는지 확인
if [ ! -f "$SRC_FILE" ]; then
  echo "❌ 오류: 복사할 원본 파일($SRC_FILE)을 찾을 수 없습니다." >&2
  exit 1
fi

# 타겟 디렉토리가 없으면 자동으로 생성
if [ ! -d "$TGT_DIR" ]; then
  echo "ℹ️ 타겟 디렉토리($TGT_DIR)가 존재하지 않아 새로 생성합니다."
  mkdir -p "$TGT_DIR" || {
    echo "❌ 오류: 타겟 디렉토리($TGT_DIR)를 생성할 수 없습니다." >&2
    exit 1
  }
fi

# 파일 복사 수행 (-p 옵션으로 파일 권한/소유 속성 유지, 덮어쓰기)
if cp -p "$SRC_FILE" "$TGT_DIR/"; then
  echo "✅ 복사 성공: $SRC_FILE -> $TGT_DIR/"
else
  echo "❌ 오류: 파일 복사 중 문제가 발생했습니다." >&2
  exit 1
fi

echo ""
echo "🎉 모든 작업이 성공적으로 완료되었습니다!"
exit 0

